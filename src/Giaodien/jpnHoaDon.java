    private void btn_XuatFileMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_XuatFileMouseClicked
        try {
            XSSFWorkbook workbook = new XSSFWorkbook();//goiworkbook
            XSSFSheet spreadsheet = workbook.createSheet("Hoá Đơn");//tao 1 sheet danh sach mang ten hoa don
            XSSFRow row = null;//hang
            Cell cell = null;//cot
            
            row = spreadsheet.createRow((short) 2);//xuong 2 hang
            row.setHeight((short) 500);
            cell = row.createCell(0, CellType.STRING);
            cell.setCellValue("DANH SÁCH HOÁ ĐON");
            
            row = spreadsheet.createRow((short) 3);
            row.setHeight((short) 500);
            cell = row.createCell(0, CellType.STRING);
            cell.setCellValue("STT");//cot 0
            
            cell = row.createCell(1, CellType.STRING);
            cell.setCellValue("Mã hoá đơn");//cot 1
            
            cell = row.createCell(2, CellType.STRING);
            cell.setCellValue("Mã khách hàng");
            
            cell = row.createCell(3, CellType.STRING);
            cell.setCellValue("Mã nhân viên");
            
            cell = row.createCell(4, CellType.STRING);
            cell.setCellValue("Ngày lập");
            
            cell = row.createCell(5, CellType.STRING);
            cell.setCellValue("Tổng tiền");
            
            //dua du lieu vao 
            List<HoaDon> listItem = DAO.DAOHoaDon.getAllHD();
            for (int i = 0; i < listItem.size(); i++) {
                HoaDon _hoadon = listItem.get(i);
                row = spreadsheet.createRow((short) 4 + i);//dua du lieu list vao hang tiep theo cua so 4 
                row.setHeight((short) 400);
                row.createCell(0).setCellValue(i + 1);//so thu tu vao cot 0 
                row.createCell(1).setCellValue(_hoadon.getMaHD());
                row.createCell(2).setCellValue(_hoadon.getMaKH());
                row.createCell(3).setCellValue(_hoadon.getMaNV());
                row.createCell(4).setCellValue(_hoadon.getNgayLap());
                row.createCell(5).setCellValue(_hoadon.getTongTien());
                //dien du lieu vao
            } 
            //luufile
            FileOutputStream out = new FileOutputStream(new File("C:\\java_luu\\Dshd.xlsx"));
            workbook.write(out);
            out.close();
            JOptionPane.showMessageDialog(this, "Xuất file thành công");
        } catch (Exception e) {
            e.printStackTrace();
        } 
    }//GEN-LAST:event_btn_XuatFileMouseClicked

