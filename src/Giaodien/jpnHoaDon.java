    private void LoadDataControl() throws SQLException, ClassNotFoundException
    {
        if (tbl_HoaDon.getSelectedRow() != -1) {
            //-- Xác định hàng đã click
            int i = tbl_HoaDon.getSelectedRow();
            
            //-- Lấy được danh sách sinh viên
            List<HoaDon> lstHoaDon = DAO.DAOHoaDon.getAllHD();
            //-- Xác định là sản phẩm  nào đang được click
            
            HoaDon sp1 = lstHoaDon.get(i);
            //--- Set giá trị cho các control
            txt_MaHD.setText(sp1.getMaHD());
            txt_MaKH.setText(sp1.getMaKH());
            txt_MaNV.setText(sp1.getMaNV());
            txt_TongTien.setText(String.valueOf(sp1.getTongTien()));
            
            DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            try {
                dpk_NgayLap.setDate(dateFormat.parse( sp1.getNgayLap()));
                
                //- Set giá trị của components JXdatepicker
            } catch (ParseException ex) {
                Logger.getLogger(jpnHoaDon.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }
   

    private void LoadDataTable_HoaDon() throws ClassNotFoundException, SQLException
    {
        List<HoaDon> lstHoaDon = DAO.DAOHoaDon.getAllHD();
        // Thêm các tiêu đề cho bảng hiẻn thị
        DefaultTableModel tblModel = new DefaultTableModel();
        tblModel.addColumn("Mã hoá đơn");
        tblModel.addColumn("Mã khách hàng ");
        tblModel.addColumn("Mã nhân viên");
        tblModel.addColumn("Ngày lập");
        tblModel.addColumn("Tổng tiền");
        
        // Add lần lượt các hàng
        for(HoaDon _hoadon : lstHoaDon)
        {
            Vector<String> row = new Vector<String>();
            row.addElement(String.valueOf(_hoadon.getMaHD()));
            row.addElement(String.valueOf(_hoadon.getMaKH()));
            row.addElement(String.valueOf(_hoadon.getMaNV())); 
            row.addElement(String.valueOf(_hoadon.getNgayLap()));
            row.addElement(String.valueOf(_hoadon.getTongTien()));
            tblModel.addRow(row);
        }
        tbl_HoaDon.setModel(tblModel);
        
        
    }

