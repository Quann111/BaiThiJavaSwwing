    private void LoadDataTable() throws SQLException, SQLException, ClassNotFoundException {
        List<NhaCungCap> lstNCC = DAO.DAONhaCungCap.getAllNCC();
        // Thêm các tiêu đề cho bảng hiẻn thị
        DefaultTableModel tblModel = new DefaultTableModel();
        tblModel.addColumn("Mã nhà cung cấp");
        tblModel.addColumn("Tên nhà cung cấp");
        tblModel.addColumn("Địa chỉ");
        tblModel.addColumn("SDT");
        // Add lần lượt các hàng
        for (NhaCungCap _NhaCungCap : lstNCC) {
            Vector<String> row = new Vector<String>();
            row.addElement(String.valueOf(_NhaCungCap.getMaNCC()));
            row.addElement(String.valueOf(_NhaCungCap.getTenNCC()));
            row.addElement(String.valueOf(_NhaCungCap.getDiaChi()));
            row.addElement(String.valueOf(_NhaCungCap.getSDT()));
            tblModel.addRow(row);
        }
        tbl_NCC.setModel(tblModel);

    }

