    private void tbl_NCCMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_NCCMouseClicked
        if (tbl_NCC.getSelectedRow() != -1) {
            //-- Xác định hàng đã click
            int i = tbl_NCC.getSelectedRow();

            //-- Lấy được danh sách sinh viên
            List<NhaCungCap> lstNC = null;
            try {
                lstNC = DAO.DAONhaCungCap.getAllNCC();
                //-- Xác định là sản phẩm  nào đang được click
            } catch (SQLException ex) {
                Logger.getLogger(jpnNhaCungCap.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(jpnNhaCungCap.class.getName()).log(Level.SEVERE, null, ex);
            }

            NhaCungCap sp1 = lstNC.get(i);
            //--- Set giá trị cho các control
            txt_MaNCC.setText(sp1.getMaNCC());
            txt_TenNCC.setText(sp1.getTenNCC());
            txt_DiaChi.setText(sp1.getDiaChi());
            txt_SDT.setText(sp1.getSDT());

        }
    }//GEN-LAST:event_tbl_NCCMouseClicked

